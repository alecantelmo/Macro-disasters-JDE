compressed_deriv(:,1)=-exp(-logk);
uncompressed_deriv=compressed_deriv(:,index.nnz{16,3});
full_vals(:,5:5)=uncompressed_deriv;
full_cols(5:5,:)=index.loc{16,3};
full_rows(5:5)=16;
derivs=sptensor(full_rows,full_cols,full_vals,16,repmat(16,1,3));
