uncompressed_deriv=compressed_deriv(:,index.nnz{14,3});
full_vals(:,3:3)=uncompressed_deriv;
full_cols(3:3,:)=index.loc{14,3};
full_rows(3:3)=14;
compressed_deriv=zeros(n_s,1);
compressed_deriv(:,1)=exp(logkp);
uncompressed_deriv=compressed_deriv(:,index.nnz{15,3});
full_vals(:,4:4)=uncompressed_deriv;
full_cols(4:4,:)=index.loc{15,3};
full_rows(4:4)=15;
