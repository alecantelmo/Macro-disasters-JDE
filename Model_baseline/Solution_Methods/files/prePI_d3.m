function derivs=prePI_d3(vars,params,index)
n_s=size(vars,1);
logcp=vars(:,1);
logc=vars(:,2);
logkp=vars(:,3);
logap=vars(:,4);
logk=vars(:,5);
loga=vars(:,6);
mpkp=vars(:,7);
