function function_f=stochtilf_fun(variables_v,parameters)
MUD=parameters(1);
RHOTHETA=parameters(2);
THETABAR=parameters(3);
SDV_THETA=parameters(4);
SDV_ZA=parameters(5);
ALPHA=parameters(6);
LAMBDA_A=parameters(7);
GAMMA=parameters(8);
NU=parameters(9);
PSI=parameters(10);
BETA=parameters(11);
DELTA=parameters(12);
PHI=parameters(13);
SCALEPARAM=parameters(14);
LOGTILUSS=parameters(15);
logtilqep=variables_v(1,:);
logtilqe=variables_v(2,:);
logqf=variables_v(3,:);
tilrp=variables_v(4,:);
tilqp=variables_v(5,:);
u01p=variables_v(6,:);
u02=variables_v(7,:);
u03=variables_v(8,:);
u04=variables_v(9,:);
logMp=variables_v(10,:);
u17=variables_v(11,:);
tildivp=variables_v(12,:);
function_f=zeros(4,size(variables_v,2));
function_f(1,:)=u02.*exp(-u01p.*(GAMMA - 1)) - 1;
function_f(2,:)=u04.*exp(u03).*(tilrp - tilqp.*(DELTA - 1)) - 1;
function_f(3,:)=exp(u17 - logtilqe).*(tildivp + exp(logtilqep)) - 1;
function_f(4,:)=exp(logMp - logqf) - 1;
